namespace LIBRERIA
{
    public class ALGEBRA
    {
        public string Hola()
        {
            return "Hola Mundo";
        }

        public double Suma(double x, double y)
        {
            return x + y;
        }

        public double Resta(double x, double y)
        {
            return x - y;
        }

        public double Producto(double x, double y)
        {
            return x * y;
        }

        public double Division(double x, double y)
        {
            return x / y;
        }
    }
}
